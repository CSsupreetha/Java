package Package1;

public class Explicit {

	public static void main(String[] args) {
		double a = 65.0;
		float b;
		int c;
		short d;
		byte e;
		long f;
		char g;
		b=(float)a;
		c=(int)a;
		d=(short)a;
		e=(byte)a;
		f=(long)a;
		g=(char)a;
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
		System.out.println(g);

	}

}
